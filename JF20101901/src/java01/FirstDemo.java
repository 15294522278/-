package java01;

public class FirstDemo {
	public static void main(String[] args) {
		/*
		 * 1.新建一个FristDemo类，写一个main方法， 定义两个int 类型的变量 a 赋值为15和 b赋值为3 ，
		 * 分别打印输出a与b的‘加/减/乘/除/取余’的结果。
		 */
		int a = 15;
		int b = 3;
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		System.out.println(a / b);
		System.out.println(a % b);

	}
}
